// Loop For

let array = [1, 5, 6, 8, 6, 10, 20, 35, 500, 150]

for (let index = 0; index < array.length; index++) {
    console.log("Número de index é: " + index)
    console.log("--------------------------------------")
    console.log("Valor da posição é: " + array[index])
    console.log("--------------------------------------")
}